package Assignment.AssignmentReviews;

public class MathOperators {
  public static void main(String[] args) {
    // You can use various math operators in java
    int num1 = 1;
    int num2 = 2;
    int num3 = 3;
    int num4 = 4;
    System.out.println(num1 + num2);
    System.out.println(num4 / num2);
    System.out.println(num3 * num2);
    System.out.println(num2 - num1);

    double two = 2.0;
    double three = 3.0;
    double result = Math.pow(two, three);
    System.out.println(result);

    double nine = 9.0;
    double squarerootOfnine = Math.sqrt(nine);
    System.out.println(squarerootOfnine);

  }
}
